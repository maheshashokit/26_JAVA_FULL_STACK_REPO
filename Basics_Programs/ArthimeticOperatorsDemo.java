class ArthimeticOperatorsDemo 
{
	public static void main(String[] args) 
	{
		int a = 10, b = 20;

		System.out.println("Arthimetic Operators Demo Program......");
		
		System.out.println("Addition Of Two No's        ::::::" + (a + b));

		System.out.println("Subtraction Of Two No's     ::::::" + (a - b));

		System.out.println("Multiplication Of Two No's  ::::::" + (a * b));

		System.out.println("Division Of Two No's        ::::::" + (a / b));//

		System.out.println("Modulo Of Two No's          ::::::" + (a % b));

	}
}
